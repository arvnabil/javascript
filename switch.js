function testingSwitch() {
    var  nilai = 10;
    switch (nilai) { //Switch Untuk menghandling nilai variabel
      case 10:
        console.log("sangat baik");
        break;
      case 8:
        console.log("baik");
        break;
      case 6:
        console.log("cukup");
        break;
        default:
        console.log("lainnya");
    }

}

testingSwitch()
// maka akan mengasilkan "Sangat Baik" Karena variabel nilai nya = 10 