function panggilReverse() {
    var kota = ["Jakarta", "Bandung", "bali", "Malang"];
    console.log(kota);
    
    var result = kota.reverse()
    return result
}

console.log(panggilReverse());

// reverse digunakan untuk membalikkan isi array dari jakarta sampai malang dibalik menjadi malang sampai jakarta
